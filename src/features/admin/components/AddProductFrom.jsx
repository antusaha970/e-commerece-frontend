import { useDispatch, useSelector } from "react-redux";
import {
  createProductAsync,
  getProductByIdAsync,
  selectAllBrands,
  selectAllCategories,
  selectProductById,
  updateProductAsync,
} from "../../product/productSlice";
import { useParams } from "react-router-dom";
import { useEffect } from "react";

